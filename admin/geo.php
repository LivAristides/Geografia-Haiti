<h6 class="fw-bold display-4">CLIMA, RELEVO, HIDROGRAFIA E VEGETAÇÃO</h6>
</div>
<div class="col-md-12 col-lg-12 col-sm-6 mx-auto text-align">
<P>O clima predominante no Haiti é o Tropical úmido, com variações locais determinadas pelo relevo. Uma das suas principais características é a elevada amplitude térmica diária e, em contrapartida, baixa variação anual.</p>
<p>As temperaturas médias anuais ficam entre 25 ºC e 30 ºC, e nos lugares mais elevados as condições são mais amenas, com mínimas de até 16 ºC. O volume de chuvas varia bastante e chega a 700 mm em encostas de montanhas. As áreas a leste são mais secas, o que se deve à presença de cadeias montanhosas, que impedem a chegada dos ventos úmidos à região.</P>
<p>O Haiti se situa em uma área de relevo jovem, com terrenos bastante montanhosos e acidentados e com, ao menos, quatro cordilheiras atravessando o país. O ponto mais elevado do Haiti fica no maciço de La Selle, na região sul. Trata-se do pico La Selle, situado a 2680 metros acima do nível do mar.</p>
<p>A vegetação natural do Haiti é formada por florestas tropicais. No entanto, as maiores áreas remanescentes dessa formação são encontradas nas áreas mais elevadas, em especial nos maciços ao sul e centro-sul. Campos e bosques são encontrados em planícies ou áreas de relevo menos acidentado, enquanto no litoral é observada a presença de mangues.</p>
<p>O rio Artibonite é o principal e maior rio do Haiti e também de toda a ilha de São Domingos, com extensão de 253 km. A maior parte dos cursos d’água que banham o país deságua no golfo de Gonâve, um golfo triangular que forma toda a costa oeste do Haiti, sendo, ainda, o maior golfo do mar do Caribe.</p>
