<div class="col-md-12 col-lg-12 col-sm-6 mx-auto text-align">
<p>Em 1697, a partir assinatura do Tratado de Ryswick envolvendo Espanha e França,
a parte ocidental da ilha foi cedida à França, e recebeu o nome de Saint Domingue.
Foi a mais importante possessão francesa nas Américas, onde ocorreu o cultivo de
cana-de-açúcar com a utilização de mão de obra escrava africana. Porém, esses
africanos escravizados, influenciados pela Revolução Francesa, rebelaram-se em
1791, liderados pelo ex-escravo e general Toussaint L’Ouverture.</p>
<p>A abolição ocorreu em 1794 e Toussaint foi nomeado governador vitalício em 1801.
No entanto, uma expedição francesa encarregada de reconquistar a ilha prendeu o
General que fora enviado para França e morreu em 1803.
Jean-Jacques Dessalines, antigo escravo, deu continuidade ao movimento de
resistência. O país obteve sua independência no dia 1° de Janeiro de 1804 e
passou a chamar-se Haiti, foi a primeira República Negra das Américas e o primeiro
país latino-americano a declarar-se independente.</p>
