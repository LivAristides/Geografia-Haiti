    <div class="container">
      <div class="col-md-12 mx-auto text-center">
<h6 class="fw-bold display-4">DITADURA</h6>
</div>
<div class="col-md-12 col-lg-12 col-sm-6 mx-auto text-align">
<p>A elite, composta por mulatos( Mulato é um termo que designa a pessoa que é
descendente de negros africanos e brancos europeus) ficou insatisfeita com a nova
política instalada no país, e, em 1806, tomou o poder após o assassinato de
Dessalines.</p>
<p>O Haiti teve sua administração fragmentada, assim, o norte ficou sob domínio de
Henri Christophe e o sul foi governado por Alexandre Pétion. Somente em 1820,
sob o governo de Jean-Pierre Boyer, o país foi unificado</p>
<p>Um dos períodos mais conturbados da história do Haiti teve início em 1957.
Naquele ano, o médico François “Papa Doc” Duvalier foi eleito presidente da nação,
instalando um regime ditatorial baseado na repressão militar que perseguiu muitos
opositores – inclusive a Igreja Católica –, e sua guarda pessoal, os tontons
macoutes (bichos-papões) eram os responsáveis pelos massacres.</p>
<p>O Papa Doc foi assassinado em 1971, no entanto, seu filho Jean-Claude Duvalier, o
Baby Doc, assumiu a presidência do Haiti, dando continuidade às perseguições
políticas. Os protestos populares contra o regime ditatorial intensificaram-se, e Baby
Doc fugiu para a França em 1986, deixando no poder uma junta chefiada pelo
general Henri Namphy</p>
<p>Sob nova constituição, o país realizou eleições presidenciais livres em 1990.
A maioria dos eleitores (67%) optou pelo padre esquerdista Jean-Bertrand Aristide.
Porém, no mesmo ano, Aristide foi deposto por um novo golpe militar e a ditadura
foi novamente imposta no país. A Organização das Nações Unidas (ONU) impôs
sanções econômicas ao Haiti para forçar a volta de Aristide. Somente em 1994,
ele retornou ao cargo de presidente do Haiti.</p>
<p>Entretanto, os problemas no Haiti persistiram, fazendo com que Aristide fugisse
para a África em fevereiro de 2004 e, atualmente, o país sofre intervenção
internacional pela ONU.
Além de todos esses entraves políticos, a população haitiana enfrenta vários
problemas de ordem socioeconômica. O Haiti é o país economicamente mais pobre
das Américas, no qual cerca de 60% da população é subnutrida e mais da metade
vive com menos de um dólar por dia.
Em Janeiro de 2010, um terremoto de magnitude 7,0 na escala Richter atingiu o
país, provocando uma série de feridos, desabrigados e mortes. Estima-se que mais
de 120 mil pessoas morreram em consequência desse desastre</p>
